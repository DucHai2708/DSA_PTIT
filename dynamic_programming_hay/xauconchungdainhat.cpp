	#include <bits/stdc++.h>
using namespace std;

using ll = long long;

int main(){
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	//code here
	int t = 1; cin >> t;
	while(t--){
		string s1, s2;
		cin >> s1 >> s2;
		int m = s1.size(), n = s2.size();
		vector<vector<int>> dp(m+1,vector<int>(n+1,0));
		for(int i = 1; i <= m; i++){
			for(int j = 1; j <= n; j++){
				if(s1[i-1] == s2[j-1]){
					dp[i][j] = dp[i-1][j-1] + 1;
				}
				else{
					dp[i][j] = max(dp[i-1][j],dp[i][j-1]);
				}
			}
		}
		cout << dp[m][n] << endl;
	}
	return 0;
}